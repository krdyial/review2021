package day12;

public class ForLoop04 {
    public static void main(String[] args) {
        //find the product of first 5 counting numbers and
        // print the product on the console

        int pro= 1;
        for (int i=1; i<=5;i++){
            pro=pro*i;
        }
        System.out.println("Product of the numbers: "+pro);


    }



}
