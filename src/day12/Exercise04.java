package day12;

public class Exercise04 {
    public static void main(String[] args) {
        /*
        4) Write a program to add counting numbers
        from 23 to 57 by using for-loop. Print the sum on the console
         */
        int sum =0;
        for (int i=23; i<=57; i++){
            sum=sum+i;
        }
        System.out.println(sum); //1400
    }
}
