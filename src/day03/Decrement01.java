package day03;

public class Decrement01 {
    public static void main(String[] args) {
        //Decrement: to decrease the value of a variable
        int num= 8;
        num-=2;
        System.out.println(num);
        num--;
        System.out.println(num);

        //We can make multiplication as well

        num*=2;
        System.out.println("Multiplication: "+num);

        num/=2;
        System.out.println("Division: "+num);
    }
}
