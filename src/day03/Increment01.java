package day03;

public class Increment01 {
    //Increment: to increase the value of a variable
    public static void main(String[] args) {

        //1.Way:
        int num1= 10;
        num1= num1+ 5;
        System.out.println(num1);

        //2.Way:
        num1+=3;
        System.out.println(num1);

        //3.Way: This way is used for increasing by 1

        num1++;
        System.out.println(num1);



    }


}
